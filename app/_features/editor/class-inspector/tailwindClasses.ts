// Convert array to options format for combobox
export const tailwindClassOptions = tailwindClasses.map((className) => ({
  label: className,
  value: className,
}));

// Function to filter classes by search term
export const filterTailwindClasses = (searchTerm: string) => {
  if (!searchTerm) return tailwindClassOptions;

  const lowerSearchTerm = searchTerm.toLowerCase();
  return tailwindClassOptions.filter((option) => option.value.toLowerCase().includes(lowerSearchTerm));
};
